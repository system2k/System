void drawImage(int sx, int sy, int width, int height, unsigned char data[]) {
	for(int y = 0; y < height; y++) {
		int ady = y + sy;
		if(ady < 0 || ady >= screenHeight) continue;
		for(int x = 0; x < width; x++) {
			int adx = x + sx;
			if(adx < 0 || adx >= screenWidth) continue;
			int sIdx = (y * width + x) * 4;
			int dIdx = (ady * screenWidth + adx) * 3;
			unsigned char r = ((float)(data[sIdx + 0] * data[sIdx + 3]) + ((float)frameBuffer[dIdx + 2] * (255 - data[sIdx + 3]))) / 255;
			unsigned char g = ((float)(data[sIdx + 1] * data[sIdx + 3]) + ((float)frameBuffer[dIdx + 1] * (255 - data[sIdx + 3]))) / 255;
			unsigned char b = ((float)(data[sIdx + 2] * data[sIdx + 3]) + ((float)frameBuffer[dIdx + 0] * (255 - data[sIdx + 3]))) / 255;
			frameBuffer[dIdx] = b;
			frameBuffer[dIdx + 1] = g;
			frameBuffer[dIdx + 2] = r;
		}
		int pos = (y + sy) / 8;
		int mpos = (y + sy) % 8;
		bitmapUpdMap[pos] |= (1 << mpos);
	}
}

void renderChar(int cx, int cy, int charCode) {
	int sx = cx * 8;
	int sy = cy * 12;
	int idx = (sy * screenWidth + sx) * 3;
	int tblIdx = charCode * 12;
	for(int y = 0; y < 12; y++) {
		unsigned char byt = fontData[tblIdx++];
		for(int x = 0; x < 8; x++) {
			unsigned char val = (byt >> x & 1);
			val *= 255;
			bitmap[x * 3 + idx] = val;
			bitmap[x * 3 + idx + 1] = val;
			bitmap[x * 3 + idx + 2] = val;
		}
		idx += screenWidth * 3;
	}
	bitmapUpd = 1;
}

void renderCharExt(int cx, int cy, int charCode, int scaleW, int scaleH, unsigned char r, unsigned char g, unsigned char b) {
	int sx = cx;
	int sy = cy;
	int tblIdx = charCode * 12;
	for(int y = 0; y < 12 * scaleH; y++) {
		unsigned char byt = fontData[tblIdx + (y / scaleH)];
		for(int x = 0; x < 8 * scaleW; x++) {
			unsigned char val = (byt >> (x / scaleW) & 1);
			if(val) {
				int idx = ((sy + y) * screenWidth + (sx + x)) * 3;
				bitmap[idx] = b;
				bitmap[idx + 1] = g;
				bitmap[idx + 2] = r;
			}
		}
		int pos = (y + sy) / 8;
		int mpos = (y + sy) % 8;
		bitmapUpdMap[pos] |= (1 << mpos);
	}
	bitmapUpd = 1;
}

void renderString(char text[], int offX, int offY, int scaleW, int scaleH, unsigned char r, unsigned char g, unsigned char b) {
	int pos = 0;
	int cx = 0;
	int cy = 0;
	while(text[pos]) {
		unsigned char code = text[pos++];
		if(code == '\n') {
			cx = 0;
			cy++;
			continue;
		}
		renderCharExt(offX + (cx * scaleW * 8), offY + (cy * scaleH * 12), code, scaleW, scaleH, r, g, b);
		cx++;
	}
}

void printText(char text[]) {
	int pos = 0;
	while(text[pos]) {
		unsigned char code = text[pos++];
		if(code == '\n') {
			curX = 0;
			curY++;
			if(curY >= 40) curY = 0;
			continue;
		}
		renderChar(curX, curY, code);
		curX++;
		if(curX >= 80) {
			curX = 0;
			curY++;
			if(curY >= 40) curY = 0;
		}
	}
}

void printInteger(int i) {
	char test[12];
	test[10] = '\n';
	test[11] = 0;
	
	unsigned int val = i;
	for(int x = 0; x < 10; x++) {
		test[9 - x] = '0' + (val % 10);
		val /= 10;
	}
	printText(test);
}

void fillBackground() {
	int ptr = 0;
	for(int y = 0; y < screenHeight; y++) {
		for(int x = 0; x < screenWidth; x++) {
			unsigned char r = 0;
			unsigned char g = 162;
			unsigned char b = 232;
			if(((y + (x / 20)) % 32 == 0) || ((x - (y / 20)) % 32 == 0)) {
				r = 0;
				g = 0;
				b = 0;
			}
			bitmap[ptr++] = b;
			bitmap[ptr++] = g;
			bitmap[ptr++] = r;
		}
	}
	bitmapUpd = 1;
}

