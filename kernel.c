void ps2_wait(unsigned char dl) {
	for(int i = 0; i < 1000; i++) {
		unsigned char inv = io_in8(0x64);
		if(inv & dl) return;
	}
}

unsigned char ps2_read() {
	ps2_wait(1);
	return io_in8(0x60);
}

void ps2_write(unsigned char val) {
	ps2_wait(2);
	io_out8(0x64, 0xD4);
	ps2_wait(2);
	io_out8(0x60, val);
	ps2_read();
}

void ps2_init() {
	int packetsize = 0;
	int samplerate = 200;
	int resolution = 1;
	
	ps2_wait(2);
	io_out8(0x64, 0xA8); // enable second port (clear bit 5 of command register? cr starts off as 0b00000101 (keyboard irq (1), system flag? (4)))
	ps2_read();
	
	ps2_wait(2);
	io_out8(0x64, 0x20); // initialize?
	ps2_wait(1);
	
	unsigned char inp1 = io_in8(0x60); // retrieve configuration byte from buffer?
	inp1 |= (1 << 1); // set bit 1 (second ps/2 port int?)
	inp1 &= ~(1 << 5); // clear bit 5 (second ps/2 port clock?)
	
	ps2_wait(2);
	io_out8(0x64, 0x60);
	ps2_wait(2);
	io_out8(0x60, inp1);
	ps2_wait(1);
	
	// restore to defaults

	ps2_write(0xF6);
	// enable Z axis
	ps2_write(0xF3);
	ps2_write(200);
	ps2_write(0xF3);
	ps2_write(100);
	ps2_write(0xF3);
	ps2_write(80);
	ps2_write(0xF2);
	unsigned char inp2 = ps2_read();
	packetsize = 3;
	if(inp2) {
		packetsize = 4;
	}
	// enable 4th and 5th buttons
	ps2_write(0xF3);
	ps2_write(200);
	ps2_write(0xF3);
	ps2_write(200);
	ps2_write(0xF3);
	ps2_write(80);
	ps2_write(0xF2);
	ps2_read();
	
	ps2_write(0xF3); // set sample rate
	ps2_write(samplerate);
	ps2_write(0xE8); // set resolution
	ps2_write(resolution);
	ps2_write(0xE6); // set scaling 1:1
	ps2_write(0xF4); // enable
}

void renderBitmapFrame() {
	unsigned int ptrPos = 0;
	for(int x = 0; x < 60; x++) {
		unsigned char upd = bitmapUpdMap[x];
		for(int z = 0; z < 8; z++) {
			unsigned char bit = (upd >> z) & 1;
			if(bit) {
				for(int s = 0; s < screenWidth * 3; s++) {
					frameBuffer[ptrPos] = bitmap[ptrPos++];
				}
			} else {
				ptrPos += screenWidth * 3;
			}
		}
		bitmapUpdMap[x] = 0;
	}
}

void rerenderFrame() {
	for(int x = 0; x < 60; x++) {
		bitmapUpdMap[x] = 0b11111111;
	}
	renderBitmapFrame();
}

